package turing.java.edu.az.miniprojects.model;

public enum DayOfWeek {
    MONDAY("Monday"),
    TUESDAY("Tuesday"),
    WEDNESDAY("WEDNESDAY"),
    THURSDAY("THURSDAY"),
    FRIDAY("FRIDAY"),
    SATURDAY("SATURDAY"),
    SUNDAY("SUNDAY");
    private final String dayOfTheWeek;

    DayOfWeek(String dayOfTheWeek) {
        this.dayOfTheWeek = dayOfTheWeek;
    }


}
